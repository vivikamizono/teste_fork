console.log("do while:")
let y = 1
do {
    console.log(y)
    y++
} while(y <= 5)

console.log("---------------------------")

console.log("while:")
let j = 1
while(j <= 5) {
    console.log(j)
    j++
}

console.log("---------------------------")

console.log("for:")
for(let i = 1; i <= 5; i++) {
    console.log(i)
}
